// Helper function to get all interests as a flat array
export const getAllInterests = (): string[] => {
  return Object.values(PersonaOptions.interests).flat()
}

// Helper function to get interests by category
export const getInterestsByCategory = (category: keyof typeof PersonaOptions.interests): string[] => {
  return PersonaOptions.interests[category] || []
}

// Helper function to search across all options
export const searchOptions = (query: string, category?: string): string[] => {
  const normalizedQuery = query.toLowerCase()
  
  if (category && category in PersonaOptions) {
    const options = PersonaOptions[category as keyof typeof PersonaOptions]
    if (Array.isArray(options)) {
      return options.filter(option => option.toLowerCase().includes(normalizedQuery))
    }
  }
  
  // Search across all categories
  const allOptions: string[] = []
  Object.values(PersonaOptions).forEach(categoryOptions => {
    if (Array.isArray(categoryOptions)) {
      allOptions.push(...categoryOptions)
    } else if (typeof categoryOptions === 'object') {
      Object.values(categoryOptions).forEach(subcategory => {
        if (Array.isArray(subcategory)) {
          allOptions.push(...subcategory)
        }
      })
    }
  })
  
  return allOptions.filter(option => option.toLowerCase().includes(normalizedQuery))
}